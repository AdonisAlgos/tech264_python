x = 5
y = 1

# Operator demonstration
addition = x + y
subtraction = x - y
multiplication = x * y
division = x / y
modulo = x % y

# Printing values from operations
print(f'Addition: {addition}\nSubtraction: {subtraction}\nMultiplication: {multiplication}\n'
      f'Division: {division}\nModeration: {modulo}')

# Printing comparison results between x and y
print(f'Greater Than: {x > y}\nLess Than: {x < y}\nEqual: {x == y}\nNot Equal: {x != y}\n'
      f'Greater Than or Equal To: {x >= y}\nLess Than or Equal To: {x <= y}')
